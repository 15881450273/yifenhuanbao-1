package com.nddmwdf.program.entity;

public class Type {
    int type_id;
    String type_content;
    String type_name;
    String type_clude;

    public int getType_id() {
        return type_id;
    }

    public void setType_id(int type_id) {
        this.type_id = type_id;
    }

    public String getType_content() {
        return type_content;
    }

    public void setType_content(String type_content) {
        this.type_content = type_content;
    }

    public String getType_name() {
        return type_name;
    }

    public void setType_name(String type_name) {
        this.type_name = type_name;
    }

    public String getType_clude() {
        return type_clude;
    }

    public void setType_clude(String type_clude) {
        this.type_clude = type_clude;
    }
}
